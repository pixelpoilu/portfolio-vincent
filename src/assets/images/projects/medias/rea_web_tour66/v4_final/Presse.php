      <td class="main" valign="top">
      Cest aux Ocean Studios situés à
Burbank, banlieue de Los Angeles plombée de soleil que Johnny a enregistré son
nouvel album, «&nbsp;Le coeur dun homme&nbsp;». Jamais un de ses albums navait
été attendu avec autant dimpatience, navait été précédé de telles rumeurs,
navait suscité de telles interrogations. Tout le monde le sait, «&nbsp;Le Cur
dun Homme&nbsp;» marque son grand retour à la musique-mère, celle que lon
trouve en filigrane douloureux tout au long de sa carrière&nbsp;: le blues.
Cest lalbum de la liberté retrouvée qui le voit replonger corps et âme dans
ces ambiances exaltées qui illuminèrent son adolescence en lui faisant
découvrir quil existait un autre monde dont Muddy Waters, John Lee Hooker,
Chuck Berry, Lightning Hopkins étaient les grands architectes et Elvis le
messager voyou flamboyant. Album de la renaissance enregistré dans un bâtiment
des années vingt (il abrita autrefois un carrossier) synthétisant un siècle de
bouleversements culturels et techniques.
      </td>
    </tr>
    <tr>
     <td colspan="2" class="main"><span style="color:#dea25f;">Technologie de pointe et matériel vintage,
vieille console analogique et micros ayant senti sur eux le souffle de Sinatra,
le meilleur des deux mondes. Lumières tamisées et températures climatisés et,
de lautre côté de cette porte anonyme, soleil aveuglant et fournaise sèche
rappellent au visiteur que le désert nest quà quelques mètres et que dès que
la nuit sera tombée, ses habitants naturels, les coyotes, senhardiront jusque
devant les maisons. Cest lOuest, ce quil en reste encore de vrai.</span>
<br><br>
«&nbsp;La grande difficulté de cet album, dit Johnny, était
de faire aujourdhui un album de blues sans avoir lair de repartir cinquante
ans en arrière.&nbsp;» Exit donc les projets de reprises, les adaptations de
grands classiques aux textes originaux percutants mais trop simplistes,
impossibles à adapter littéralement sans sombrer dans le cliché et la
caricature. Pour lui écrire des titres nouveaux, mais qui puiseraient leurs
forces non dans la nostalgie mais dans les sources vives les plus
contemporaines, Johnny fit appel à des gens qui vibraient aux mêmes choses que
lui&nbsp;: Yvan Cassar, son directeur musical, les vieux compagnons de
bourlingue Pierre Billon et Michel Mallory, Fred Blondin, Didier Golemanas,
Jacques Veneruso, le romancier Marc Lévy, le comédien Bruno Putzulu. Lionel
Florence adapta en français une chanson originale de Bernie Taupin et Jim
Cregan et Francis Cabrel accepta volontiers quun de ses titres les plus
mythiques, «&nbsp;Sarbacane&nbsp;», soit revisité. Bono enfin, en congé
provisoire de U2, écrivit spécialement une chanson pour lui «&nbsp;I am the Blues&nbsp;».
<br><br>
<span style="color:#dea25f;">
Ces nouveaux habits bluesy et country confectionnés sur
mesure, il fallait aussi choisir les musiciens pour leur donner son et vie. A
la démonstration de force, au générique réunissant les plus grandes stars
populaires du genre que lui permettait sa notoriété et son carnet dadresses,
Johnny a privilégié lauthenticité et léclectisme. On retrouve donc à ses
cotés Taj Mahal dont les albums sont des joyaux qui marquent le chemin du blues
contemporain depuis quarante ans, et KebMo qui représente la nouvelle
génération des bluesmen américains. Le son de la guitare de son ami  Tony Joe
White fait tomber en nappes ouateuses latmosphère trouble des bayous de la
Louisiane. Paul Personne que sa phobie de lavion empêche de voyager est venu
rajouter quelques parties de guitares virtuoses dans un studio parisien et
Freddy Koella, guitariste dorigine strasbourgeoise qui fut longtemps le
compagnon musical de Dylan est là aussi. Ainsi que Claude Engel, Denis
Benarrosh, Eric Sauviat, Laurent Vernerey, Robin Lemesurier.</span>
<br><br>
Lalbum, réalisé par Yvan Cassar, fut mixé par Bob
Clearmountain dont les collaborations avec Bowie, les Stones ou Bruce
Springsteen ont marqué lhistoire de la musique populaire.
<br><br><<span style="color:#dea25f;">
Mais cette Dream Team nest réellement performante que parce
quelle est au service dun des plus grands artistes de notre temps qui a
retrouvé, en revisitant un territoire dont il était resté éloigné
discographiquement depuis trop longtemps, sa pleine puissance et son panache.</span>
<br><br>
Nous lentendons ici tel quen lui-même, sans filet, à vif,
toutes émotions dehors. Le vrai Johnny. Enfin . . .
